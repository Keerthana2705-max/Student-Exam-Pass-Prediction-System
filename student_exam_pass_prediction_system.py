import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.preprocessing import LabelEncoder, StandardScaler
from sklearn.model_selection import train_test_split
from sklearn.linear_model import LogisticRegression

from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score,
    confusion_matrix,
    classification_report,
    roc_auc_score
)

"""LOAD DATASET"""

df = pd.read_csv("/content/drive/MyDrive/Colab Notebooks/Students Performance .csv")

print("Dataset Shape:", df.shape)
print(df.head())

""" DATA CLEANING"""

print("\nMissing Values")
print(df.isnull().sum())

df.drop_duplicates(inplace=True)

"""DESCRIPTIVE STATISTICS"""

print("\nAverage Weekly Study Hours:")
print(df["Weekly_Study_Hours"].mean())

print("\nGender Distribution")
print(df["Sex"].value_counts())

df['Pass_Fail'] = df['Grade'].apply(lambda x: 0 if x == 0 else 1)

print('\nUnique Grades and their counts:')
print(df['Grade'].value_counts())

print('\nPass vs Fail Distribution (after redefinition):')
print(df['Pass_Fail'].value_counts())

X = df.drop(
    ["Pass_Fail"],
    axis=1
)

y = df["Pass_Fail"]

# Feature Scaling
scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)

# Train Test Split
X_train, X_test, y_train, y_test = train_test_split(
    X_scaled,
    y,
    test_size=0.2,
    random_state=42
)

# Model
model = LogisticRegression(max_iter=1000)

model.fit(X_train, y_train)

# Predictions
y_pred = model.predict(X_test)

# Probability
y_prob = model.predict_proba(X_test)[:,1]

print("Model training complete. Proceeding to evaluation...")

print("\nAccuracy:",accuracy_score(y_test, y_pred))
print("Precision:",precision_score(y_test, y_pred))
print("Recall:",recall_score(y_test, y_pred))
print("F1 Score:",f1_score(y_test, y_pred))
print("ROC AUC:",roc_auc_score(y_test, y_prob))
print("\nClassification Report")
print(classification_report(y_test, y_pred))

cm = confusion_matrix(y_test, y_pred)

plt.figure(figsize=(6,4))

sns.heatmap(
    cm,
    annot=True,
    fmt='d',
    cmap='Blues'
)

plt.title("Confusion Matrix")
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.show()

