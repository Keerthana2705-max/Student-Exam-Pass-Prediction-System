sns.countplot(x="Grade", data=df)
plt.title("Grade Distribution")
plt.show()

"""Encoding"""

le = LabelEncoder()

for col in df.columns:
    # Only encode object type columns, excluding 'Student_ID' if it still exists and was not meant to be encoded
    if df[col].dtype == "object":
        df[col] = le.fit_transform(df[col])

# Remove Student ID only if it exists in the DataFrame
if 'Student_ID' in df.columns:
    df.drop("Student_ID", axis=1, inplace=True)
    print("Student_ID column dropped.")
else:
    print("Student_ID column not found in DataFrame (already dropped or never existed).")

"""Correlation Heatmap"""

plt.figure(figsize=(12,8))

sns.heatmap(
    df.corr(),
    annot=True,
    cmap="coolwarm"
)

plt.title("Correlation Heatmap")
plt.show()

"""Risk Analysis"""

def risk_level(row):

    if row["Weekly_Study_Hours"] <= 2:
        return "High Risk"

    elif row["Weekly_Study_Hours"] <= 8:
        return "Medium Risk"

    else:
        return "Low Risk"

df["Risk_Category"] = df.apply(
    risk_level,
    axis=1
)

print("\nRisk Category Count")
print(df["Risk_Category"].value_counts())

# Encode Risk Category
df["Risk_Category"] = LabelEncoder().fit_transform(
    df["Risk_Category"]
)

"""Logistic Regression"""

X = df.drop(
    ["Pass_Fail"],
    axis=1
)

y = df["Pass_Fail"]

# Feature Scaling
scaler = StandardScaler()

X_scaled = scaler.fit_transform(X)

# Train Test Split
X_train, X_test, y_train, y_test = train_test_split(
    X_scaled,
    y,
    test_size=0.2,
    random_state=42
)

# Model
model = LogisticRegression(max_iter=1000)

model.fit(X_train, y_train)

# Predictions
y_pred = model.predict(X_test)

# Probability
y_prob = model.predict_proba(X_test)[:,1]

"""Model Evaluation"""

print("\nAccuracy:",accuracy_score(y_test, y_pred))
print("Precision:",precision_score(y_test, y_pred))
print("Recall:",recall_score(y_test, y_pred))
print("F1 Score:",f1_score(y_test, y_pred))
print("ROC AUC:",roc_auc_score(y_test, y_prob))
print("\nClassification Report")
print(classification_report(y_test, y_pred))

"""Confusion Matrix"""

cm = confusion_matrix(y_test, y_pred)

plt.figure(figsize=(6,4))

sns.heatmap(
    cm,
    annot=True,
    fmt='d',
    cmap='Blues'
)

plt.title("Confusion Matrix")
plt.xlabel("Predicted")
plt.ylabel("Actual")
plt.show()